// Popular local bank names by country (simplified - using generic names)
const getLocalBankNames = (countryName) => {
  const genericNames = [
    `${countryName} Commercial Bank`,
    `${countryName} Savings Bank`,
    `${countryName} Trust Bank`,
    `${countryName} Credit Union`,
    `${countryName} Community Bank`
  ];
  return genericNames;
};

async function ensureWorldBank(db) {
  const worldBanksCollection = db.collection('world_banks');
  
  const existing = await worldBanksCollection.findOne({ name: "Crypto World Bank" });

  if (!existing) {
    const result = await worldBanksCollection.insertOne({
      name: "Crypto World Bank",
      total_reserve: 1000000, // 1M ETH
      created_at: new Date(),
      updated_at: new Date()
    });
    console.log("✅ Created World Bank with 1,000,000 ETH reserve");
    return result.insertedId;
  }

  await worldBanksCollection.updateOne(
    { _id: existing._id },
    { 
      $set: { 
        total_reserve: 1000000,
        updated_at: new Date()
      }
    }
  );
  console.log("✅ Updated World Bank reserve to 1,000,000 ETH");
  return existing._id;
}

async function seedBanks(db, worldBankId) {
  console.log("🌍 Seeding national and local banks for demo...");

  const nationalBanksCollection = db.collection('national_banks');
  const localBanksCollection = db.collection('local_banks');

  for (const country of COUNTRIES) {
    // Skip if we already have a national bank for this country
    const existingNational = await nationalBanksCollection.findOne({ country: country.code });

    if (existingNational) {
      continue;
    }

    const nationalName = `${country.name} National Bank`;
    const nationalAddress = `${country.name} Capital`;

    const nationalResult = await nationalBanksCollection.insertOne({
      world_bank_id: worldBankId,
      name: nationalName,
      country: country.code,
      address: nationalAddress,
      total_borrowed: 100, // 100 ETH
      total_lent: 50, // 5 local banks × 10 ETH each
      created_at: new Date(),
      updated_at: new Date()
    });

    const nationalBankId = nationalResult.insertedId;

    // Create 5 local banks with 10 ETH each
    const localBankNames = getLocalBankNames(country.name);
    const cities = ['Capital', 'Metro', 'Coastal', 'Inland', 'Northern'];

    for (let i = 0; i < 5; i++) {
      const localName = localBankNames[i];
      const city = `${cities[i]} City`;
      const localAddress = `${localName} Branch`;

      await localBanksCollection.insertOne({
        national_bank_id: nationalBankId,
        name: localName,
        city: city,
        address: localAddress,
        total_borrowed: 10, // 10 ETH
        total_lent: 0,
        created_at: new Date(),
        updated_at: new Date()
      });
    }

    console.log(
      `✅ Seeded national bank for ${country.name} with 5 local banks (100 ETH national, 10 ETH each local)`
    );
  }
}

async function seed() {
  try {
    console.log("🌱 Starting database seeding...");

    const db = await getDB();
    const worldBankId = await ensureWorldBank(db);
    await seedBanks(db, worldBankId);

    console.log("✅ Seeding completed!");
    console.log("\n📝 Demo Balances:");
    console.log("   - Crypto World Bank: 1,000,000 ETH (off-chain demo value)");
    console.log("   - Each National Bank: 100 ETH");
    console.log("   - Each Local Bank: 10 ETH");
    console.log(`\n📊 Total: ${COUNTRIES.length} National Banks, ${COUNTRIES.length * 5} Local Banks`);

    process.exit(0);
  } catch (error) {
    console.error("❌ Seeding failed:", error);
    process.exit(1);
  }
}

seed();
